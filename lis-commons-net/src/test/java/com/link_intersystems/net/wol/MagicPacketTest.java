package com.link_intersystems.net.wol;

import com.link_intersystems.net.MAC;
import org.junit.jupiter.api.Test;

class MagicPacketTest {

    @Test
    void toByteArray() {
        MagicPacket magicPacket = new MagicPacket(new MAC(new byte[]{0x34, 0x2E, (byte) 0xB7, 0x0B, 0x4E, 0x60}));

        ByteArrayAssertion byteArrayAssertion = new ByteArrayAssertion(magicPacket.toByteArray());

        byteArrayAssertion.assertEquals(0, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF);

        byteArrayAssertion.assertEquals(6, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(12, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(18, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(24, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(30, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(36, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(42, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(48, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(54, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(60, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(66, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(72, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(78, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(84, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(90, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);
        byteArrayAssertion.assertEquals(96, 0x34, 0x2E, 0xB7, 0x0B, 0x4E, 0x60);

    }


}